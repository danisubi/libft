int	ft_isalpha(char *str)
{
	if(*str == '\0')
		return (1);
	while (*str !='\0')
	{
		if((*str >='a' && *str <= 'z')
			|| (*str >= 'A' && *str <= 'Z'))
			str++;
		else
			return (0);
	}
	return (1);
}
